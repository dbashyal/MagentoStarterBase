/**
 * admin product edit tabs
 *
 * @category   Technooze
 * @package    Technooze_Stores
 * @author     Technooze <info@technooze.com>
 */
class Technooze_Stores_Block_Adminhtml_Catalog_Product_Edit_Tabs extends Mage_Adminhtml_Block_Catalog_Product_Edit_Tabs
{
    protected function _prepareLayout()
    {
        parent::_prepareLayout();

        $this->addTab('stores', array(
            'label'     => Mage::helper('catalog')->__('Retailer Stores'),
            'url'       => $this->getUrl('*/*/crosssell', array('_current' => true)),
            'class'     => 'ajax',
        ));

        return;
    }
}
